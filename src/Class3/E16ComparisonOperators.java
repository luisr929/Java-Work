package Class3;

public class E16ComparisonOperators {
    public static void main(String[] args) {
        int one=10;
        int two=20;
        boolean result=one>two;
        System.out.println(result);
        System.out.println(one>two);
        System.out.println(one<two);
        System.out.println(one==two);
        System.out.println(one!=two);
        System.out.println(one>=two);
        System.out.println(two<=one);
    }
}
