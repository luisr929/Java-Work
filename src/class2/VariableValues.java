package class2;

public class VariableValues {
    public static void main(String[] args) {
        byte age=10;
        age=20;
        System.out.println(age);
        short shoeSize=9;
        shoeSize=10;
        System.out.println(shoeSize);
        int salary=90000;
        salary=100000;
        System.out.println(salary);
        long travel=51215155451215L;
        travel=778855556565564454L;
        System.out.println(travel);
        float grams=3.5F;
        grams=7.0F;
        System.out.println(grams);
        double length=7.2;
        length=9.2;
        System.out.println(length);
        char currency='$';
        currency='@';
        System.out.println(currency);
        boolean areYouWarm=true;
        areYouWarm=false;
        System.out.println(areYouWarm);



    }
}
